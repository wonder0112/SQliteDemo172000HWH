package com.example.stu.sqlitedemo172000hwh;

public class Infor {
    private  int _id;
    private String name;
    private Long phone;
    public Infor() {    }
    public int get_id() {
        return _id;
    }
    public void set_id(int _id) {
        this._id = _id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public Long getPhone() {
        return phone;
    }
    public void setPhone(Long phone) {
        this.phone = phone;
    }
}
